package corsoJavaBackend.Settimana1.lezione2_24_06_2025.ProveSystem;

public class ProveSystem {
    public static void main(String[] args) {
        // Stampa stringhe
        System.out.println("Hello World!");
        System.out.println("Tutto OK!");
        // System.out.println(sbagliato!); Le String in generale necessitano l'apertura
        // e la chiusura dei doppi apici
        System.out.print("Hello World! ");
        System.out.print("I will print on the same line.\n");

        // stampa numeri
        System.out.println(3);
        System.out.println(358);
        System.out.println(50_000);
        System.out.println(3 + 3);
        System.out.println(3 * 2);
    }

}
